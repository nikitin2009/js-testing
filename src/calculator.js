const calculator = {
  add: function(a, b) {
    let result = a + b;
    return result;
  },
  subtract: function(a, b) {
    let result = a - b;
    return result;
  },
  divide: function(a, b) {
    let result = a / b;
    return result;
  },
  multiply: function(a, b) {
    let result = a * b;
    return result;
  }
}

module.exports = calculator;